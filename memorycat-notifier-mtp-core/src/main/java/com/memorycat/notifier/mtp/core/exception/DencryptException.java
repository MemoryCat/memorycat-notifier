package com.memorycat.notifier.mtp.core.exception;

public class DencryptException extends MemoryCatNotifierException {

	private static final long serialVersionUID = -4178630087875144978L;

	public DencryptException() {
		super();
	}

	public DencryptException(String message, Throwable cause) {
		super(message, cause);
	}

	public DencryptException(String message) {
		super(message);
	}

	public DencryptException(Throwable cause) {
		super(cause);
	}

}
